import java.util.*;
import java.lang.*;
import java.io.*;

// 그대로 출력하기
// https://www.acmicpc.net/problem/11718
class BOJ11718 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        while (true) {
            String s = sc.nextLine();
            System.out.println(s);
            if (!sc.hasNext()) {
                break;
            }
        }
    }
}